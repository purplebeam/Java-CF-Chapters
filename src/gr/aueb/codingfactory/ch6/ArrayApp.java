package gr.aueb.codingfactory.ch6;

/**
 *  Array Demo.
 */
public class ArrayApp {
    public static void main(String[] args) {
        int[] arr = new int[3];
        arr[0] = 1;
        arr[1] = 2;
        arr[2] = 3;

        System.out.println("Arr[0] =" + arr[0]);
        System.out.println("Arr[1] =" + arr[1]);
        System.out.println("Arr[2] =" + arr[2]);
    }
}
