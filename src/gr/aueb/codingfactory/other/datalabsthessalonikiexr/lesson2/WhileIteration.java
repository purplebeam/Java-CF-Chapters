package gr.aueb.codingfactory.other.datalabsthessalonikiexr.lesson2;

public class WhileIteration {

    public static void main(String[] args) {

        int num = 0;

        while (num<10){
            System.out.println("Hello World");

            num++;
        }

        do {
            System.out.println("GoodBye World");

        } while (num>10);
    }
}
