package gr.aueb.codingfactory.other.datalabsthessalonikiexr.lesson2;

public class IterationWhileUkn {

    public static void main(String[] args) {

        double sum = 0;
        double n = 1;

        while(sum<=5){
            sum = sum +1/n;
            n++;
        }

        System.out.println(n);
    }
}
